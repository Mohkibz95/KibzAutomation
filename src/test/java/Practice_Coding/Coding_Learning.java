package Practice_Coding;

public class Coding_Learning {

    public static void main(String[] args) {
        //Coding with escape sequences
        // \n is similar to the 'ln' in println

        System.out.print("I love hoorulain!\n");
        System.out.print("I love hoorulain!");

        //If you want to add another empty line then mention both
        System.out.println("I love hoorulain!\n");
        System.out.println("I love hoorulain!");

        //To add tab mention \t
        System.out.println("\tI love hoorulain!");
        System.out.println("I love hoorulain!");

        //To add double quotes in a sentence mention \" in the beginning and \" in the end.
        System.out.println("\"I love hoorulain!\"");

        //If I want to mention comments then I will start with //. If I want to do a multi-comments then I will use -
        //a forward slash and asterisks for example:
        /*
        *This
        *will
        *do
        *nothing if I printed.
        */

        //If I want to save time then I can also write "sout" and press tab, as opposed to typing out System.out.println










































































































    }
}
